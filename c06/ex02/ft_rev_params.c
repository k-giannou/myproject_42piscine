#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	while ((argc - 1) > 0)
	{
		i = 0;
		while (argv[argc - 1][i] != '\0')
			i++;
		write (1, argv[argc - 1], i);
		write (1, "\n", 1);
		argc--;
	}
}
